/* Comparison operators are of two types 
relational operator >, >=, <, <=
Equaility operator
=== strict equility (value + type)
!== strict inequility (value + type)
== loose equility (value)
!= loose inequility (value)
*/
let firstFavNum = 21;
let secondFavNum = 10;
console.log(firstFavNum > secondFavNum);
console.log(firstFavNum < secondFavNum);
console.log(firstFavNum >= secondFavNum);
console.log(firstFavNum <= secondFavNum);
console.log(firstFavNum === secondFavNum);
console.log(firstFavNum !== secondFavNum);
console.log(firstFavNum == secondFavNum);
console.log(firstFavNum != secondFavNum);
